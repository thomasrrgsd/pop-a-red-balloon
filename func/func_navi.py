def goto_position_target_local_ned(vehicle, x_velocity):
    """
    Send SET_POSITION_TARGET_LOCAL_NED command to request the vehicle fly to a specified
    location in the x velocity frame.
    
    https://dronekit-python.readthedocs.io/en/latest/examples/guided-set-speed-yaw-demo.html
    
    https://ardupilot.org/dev/docs/copter-commands-in-guided-mode.html
    
    """
    msg = vehicle.message_factory.set_position_target_local_ned_encode(
        0,       # time_boot_ms (not used)
        0, 0,    # target system, target component
        mavutil.mavlink.MAV_FRAME_LOCAL_NED, 
        0b110111000111, # type_mask (only velocities enabled)
        0, 0, 0,
        x_velocity, 0, 0, # x, y, z velocity in m/s 
        0, 0, 0, # x, y, z acceleration (not supported yet, ignored in GCS_Mavlink)
        0, 0)    # yaw, yaw_rate (not supported yet, ignored in GCS_Mavlink)
    # send command to vehicle
    vehicle.send_mavlink(msg)
